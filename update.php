function encode_image($doc)
{
	$url = '';
	
	if ($url == '')
	{
		if (isset($doc->message->thumbnailUrl) && !isset($doc->message->image))
		{
			$url = $doc->message->thumbnailUrl;
		}
	}

	if ($url == '')
	{
		if (isset($doc->message->image) && preg_match('/^http/', $doc->message->image))
		{
			$url = $doc->message->image;
		}
	}

	if ($url != '')
	{
		// get image URL		
		$image = get($url);
		
		if ($image != '')
		{
			// store image in a temporary file
			$image_file_name = tempnam("/tmp", "image");			
			file_put_contents($image_file_name, $image);
			
			// get MIME type
			$finfo = finfo_open(FILEINFO_MIME_TYPE); // return mime type ala mimetype extension
			$mime_type = finfo_file($finfo, $image_file_name);
			finfo_close($finfo);
			
			// resize
			$command = 'mogrify -resize 128 ' . $image_file_name;
			system($command);

			// encode 			
			$image = file_get_contents($image_file_name);
			$base64 = chunk_split(base64_encode($image));
			
			// save image URL then replace image
			if (!isset($doc->message->thumbnailUrl))
			{
				$doc->message->thumbnailUrl = $url;
			}			
			$doc->message->image = 'data:' . $mime_type . ';base64,' . $base64;	
		}
	}
	return $doc;
}


