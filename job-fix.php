foreach ($ids as $id)
{
	$doc = fetch($id);
	
	$modified = false;
	
	//print_r($doc);
	
	if (isset($doc->message->{'@type'}))
	{
		unset($doc->message->{'@type'});
		$modified = true;
	}
	
	if (isset($doc->message->{'@id'}))
	{
		$doc->message->id = $doc->message->{'@id'};
		unset($doc->message->{'@id'});
		$modified = true;
	}
	
	if (!isset($doc->message->item))
	{
		$doc->message->item = new stdclass;
		
		$modified = true;
	}
	else
	{
		// we have item already
		if (isset($doc->message->item->{'@type'}))
		{
			unset($doc->message->item->{'@type'});
			$modified = true;
		}
	
		if (isset($doc->message->item->{'@id'}))
		{
			$doc->message->item->id = $doc->message->item->{'@id'};
			unset($doc->message->item->{'@id'});
			$modified = true;
		}
		
	
	}
	
	$keys = array('author', 'datePublished', 'doi', 'issueNumber', 'name', 'pageStart', 'pageEnd', 'pmid', 'url', 'volumeNumber');
	
	foreach ($keys as $k)
	{
		if (!isset($doc->message->item->{$k}) && isset($doc->message->{$k}))
		{
			$doc->message->item->{$k} = $doc->message->{$k};
			
			switch ($k)
			{
				// delete keys that are only relevant to the item
				case 'author':
				case 'doi':
				case 'issueNumber':
				case 'pageStart':
				case 'pageEnd':
				case 'pmid':
				case 'volumeNumber':
					unset($doc->message->{$k});
					break;				
			
				default:
					break;
			}
			
			$modified = true;
		}
	}
	
	// DOI as identifier
	if (isset($doc->message->item->doi) && !isset($doc->message->item->id))
	{
		$doc->message->item->id = 'https://doi.org/' . $doc->message->item->doi;
		$modified = true;
	}
	
	
	
	if ($modified)
	{
		print_r($doc);
		store($doc->message, true);
	}

}


